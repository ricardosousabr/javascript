function somar(valorSomar) {
  return valorSomar + valorSomar
};

function subtrair(valorSubtrair) {
  return valorSubtrair - valorSubtrair
};

function dividir(valorDividir) {
  return valorDividir / valorDividir
};

function multiplicar(valorMultiplicar) {
  return valorMultiplicar * valorMultiplicar
};

console.log(somar(10), subtrair(5), dividir(50), multiplicar(10))